import { buildEpicDocumentReference, validateDocumentReferenceSchema } from './fhir-document-reference.js';

function clean(value, max = 4000) {
  return String(value || '').trim().slice(0, Math.max(1, Number(max) || 1));
}

function asObject(value) {
  return value && typeof value === 'object' && !Array.isArray(value) ? value : {};
}

function toBase64Utf8(value) {
  return Buffer.from(clean(value, 30_000), 'utf-8').toString('base64');
}

function normalizeConnector(value) {
  const normalized = clean(value, 40).toLowerCase();
  if (normalized === 'epic' || normalized === 'athena' || normalized === 'both') {
    return normalized;
  }
  return 'both';
}

function normalizeMode(value) {
  const normalized = clean(value, 40).toLowerCase();
  if (normalized === 'live') {
    return 'live';
  }
  if (normalized === 'dry-run' || normalized === 'dryrun' || normalized === 'preview') {
    return 'dry-run';
  }
  return '';
}

function joinUrl(baseUrl, path) {
  const base = clean(baseUrl, 2000).replace(/\/+$/, '');
  const suffix = clean(path, 1000).replace(/^\/+/, '');
  if (!base) {
    return '';
  }
  if (!suffix) {
    return base;
  }
  return `${base}/${suffix}`;
}

function buildAttachment(packet = {}, attachment = {}) {
  const caseId = clean(packet.case_id || packet.caseId || 'UNKNOWN-CASE', 120);
  const procedure = clean(packet.procedure || 'Prior authorization request', 200);
  const diagnosis = clean(packet.diagnosis, 200);
  const payer = clean(packet.payer_name || packet.payerName || 'Unknown payer', 120);
  const summary = clean(
    packet.readiness_summary ||
      packet.summary ||
      packet.recommended_action ||
      'Submission-prep packet generated by AuthPilot AI.',
    3000,
  );

  const narrative = [
    `Case: ${caseId}`,
    `Payer: ${payer}`,
    `Procedure: ${procedure}`,
    diagnosis ? `Diagnosis: ${diagnosis}` : '',
    `Submission readiness: ${packet.submission_ready ? 'ready' : 'collecting_evidence'}`,
    `Summary: ${summary}`,
  ]
    .filter(Boolean)
    .join('\n');

  const providedBase64 = clean(attachment.base64 || attachment.data, 500_000);
  const contentType =
    clean(attachment.contentType, 120) || (providedBase64 ? 'application/pdf' : 'text/plain; charset=utf-8');
  const title =
    clean(attachment.title, 200) || `${caseId}-submission-prep.${contentType.includes('pdf') ? 'pdf' : 'txt'}`;

  return {
    title,
    contentType,
    data: providedBase64 || toBase64Utf8(narrative),
  };
}

export function buildEpicSubmitAttachmentPrototype({ packet = {}, epic = {}, attachment = {} } = {}) {
  const cfg = asObject(epic);
  const builtAttachment = buildAttachment(packet, attachment);
  const caseId = clean(packet.case_id || packet.caseId || 'UNKNOWN-CASE', 120);

  const docRef = buildEpicDocumentReference(packet);
  docRef.content = [
    {
      attachment: builtAttachment,
    },
  ];

  const schemaCheck = validateDocumentReferenceSchema(docRef);

  const parameters = {
    resourceType: 'Parameters',
    parameter: [
      {
        name: 'documentReference',
        resource: docRef,
      },
      {
        name: 'trackingId',
        valueString: caseId,
      },
      {
        name: 'sourceSystem',
        valueString: 'AuthPilot AI',
      },
    ],
  };

  const baseUrl = clean(cfg.baseUrl || process.env.EPIC_FHIR_BASE_URL, 1000);
  const endpointPath = clean(cfg.endpointPath || process.env.EPIC_SUBMIT_ATTACHMENT_PATH || '/DocumentReference/$submit-attachment', 500);
  const accessToken = clean(cfg.accessToken || process.env.EPIC_ACCESS_TOKEN, 5000);

  return {
    connector: 'epic',
    operation: 'submit_attachment',
    schemaCheck,
    request: {
      method: 'POST',
      url: joinUrl(baseUrl, endpointPath),
      headers: {
        'content-type': 'application/fhir+json',
        ...(accessToken ? { authorization: `Bearer ${accessToken}` } : {}),
      },
      body: parameters,
      hasAuthorization: Boolean(accessToken),
    },
  };
}

function buildAthenaClinicalDocumentBody(packet = {}, attachment = {}, athena = {}) {
  const cfg = asObject(athena);
  const builtAttachment = buildAttachment(packet, attachment);

  return {
    departmentid: clean(cfg.departmentId || packet.department_id || '1', 40),
    documentclass: clean(cfg.documentClass || 'CLINICAL', 80),
    documentsubclass: clean(cfg.documentSubclass || 'AUTHORIZATION', 80),
    description: clean(
      cfg.description ||
        packet.readiness_summary ||
        `AuthPilot submission-prep packet for ${clean(packet.procedure || 'prior auth', 120)}`,
      1000,
    ),
    document: {
      filename: builtAttachment.title,
      contenttype: builtAttachment.contentType,
      base64: builtAttachment.data,
    },
  };
}

function buildAthenaTaskBody(packet = {}, athena = {}, clinicalDocumentId = '{{clinicalDocumentId}}') {
  const cfg = asObject(athena);
  const caseId = clean(packet.case_id || packet.caseId || 'UNKNOWN-CASE', 120);
  const procedure = clean(packet.procedure || 'Prior authorization case', 160);

  return {
    patientid: clean(cfg.patientId || packet.patient_id, 80),
    subject: clean(cfg.taskSubject || `Prior auth packet ready: ${procedure}`, 200),
    priority: clean(cfg.priority || 'HIGH', 30),
    assignedto: clean(cfg.assigneeRole || 'Authorization Coordinator', 120),
    dueDate: clean(cfg.dueDate || '', 80),
    note: clean(
      cfg.taskNote ||
        `AuthPilot prepared submission packet for case ${caseId}. Review the uploaded clinical document and submit in payer portal.`,
      2000,
    ),
    references: [
      {
        type: 'clinicaldocument',
        id: clean(clinicalDocumentId, 120),
      },
    ],
  };
}

export function buildAthenaFirstWritePrototype({ packet = {}, athena = {}, attachment = {} } = {}) {
  const cfg = asObject(athena);
  const baseUrl = clean(cfg.baseUrl || process.env.ATHENAHEALTH_BASE_URL || 'https://api.athenahealth.com', 1000);
  const practiceId = clean(cfg.practiceId || packet.practice_id || process.env.ATHENAHEALTH_PRACTICE_ID, 80);
  const patientId = clean(cfg.patientId || packet.patient_id, 80);
  const accessToken = clean(cfg.accessToken || process.env.ATHENAHEALTH_ACCESS_TOKEN, 5000);

  const clinicalDocumentPath = `v1/${practiceId}/patients/${patientId}/documents/clinicaldocument`;
  const taskPath = clean(cfg.tasksPath || '/tasks', 200);

  const clinicalDocumentBody = buildAthenaClinicalDocumentBody(packet, attachment, cfg);
  const taskBodyPreview = buildAthenaTaskBody(packet, cfg);

  return {
    connector: 'athena',
    operation: 'clinicaldocument_and_task',
    requestChain: [
      {
        operation: 'upload_clinical_document',
        method: 'POST',
        url: practiceId && patientId ? joinUrl(baseUrl, clinicalDocumentPath) : '',
        headers: {
          'content-type': 'application/json',
          ...(accessToken ? { authorization: `Bearer ${accessToken}` } : {}),
        },
        body: clinicalDocumentBody,
        hasAuthorization: Boolean(accessToken),
      },
      {
        operation: 'create_task',
        method: 'POST',
        url: joinUrl(baseUrl, taskPath),
        headers: {
          'content-type': 'application/json',
          ...(accessToken ? { authorization: `Bearer ${accessToken}` } : {}),
        },
        body: taskBodyPreview,
        hasAuthorization: Boolean(accessToken),
      },
    ],
    prerequisites: {
      requiresPracticeId: Boolean(practiceId),
      requiresPatientId: Boolean(patientId),
    },
  };
}

function summarizeResponseBody(body) {
  const payload = asObject(body);
  return {
    id: clean(payload.id || payload.documentid || payload.taskid || payload.referenceId || ''),
    status: clean(payload.status || payload.state || payload.result || ''),
    message: clean(
      payload.message ||
        payload.error ||
        payload.error_description ||
        payload.detail ||
        payload.title ||
        payload.issue?.[0]?.diagnostics ||
        payload.issue?.[0]?.details?.text ||
        '',
      500,
    ),
  };
}

function extractAthenaClinicalDocumentId(body) {
  const payload = asObject(body);
  return (
    clean(payload.documentid || payload.documentId || payload.id, 120) ||
    clean(payload.document?.id || payload.document?.documentid, 120)
  );
}

async function executeRequest(request) {
  const response = await fetch(request.url, {
    method: request.method,
    headers: request.headers,
    body: JSON.stringify(request.body),
  });

  const rawText = await response.text();
  let parsed = null;
  if (rawText) {
    try {
      parsed = JSON.parse(rawText);
    } catch {
      parsed = { message: rawText.slice(0, 500) };
    }
  }

  return {
    ok: response.ok,
    status: response.status,
    summary: summarizeResponseBody(parsed),
    body: parsed,
  };
}

export async function runConnectorPrototype({
  connector = 'both',
  packet = {},
  dryRun = true,
  attachment = {},
  epic = {},
  athena = {},
  packetId = '',
  operatorId = '',
} = {}) {
  const connectorMode = normalizeConnector(connector);
  const connectors = connectorMode === 'both' ? ['epic', 'athena'] : [connectorMode];

  const previews = [];
  if (connectors.includes('epic')) {
    previews.push(buildEpicSubmitAttachmentPrototype({ packet, epic, attachment }));
  }
  if (connectors.includes('athena')) {
    previews.push(buildAthenaFirstWritePrototype({ packet, athena, attachment }));
  }

  if (dryRun) {
    return {
      dryRun: true,
      connector: connectorMode,
      packetId: clean(packetId || packet.case_id || packet.caseId, 120),
      operatorId: clean(operatorId, 120),
      previews,
      executed: [],
      externalIds: [],
    };
  }

  const executed = [];
  const externalIds = [];

  for (const preview of previews) {
    if (preview.connector === 'epic') {
      const req = preview.request;
      if (!req.url || !req.hasAuthorization) {
        executed.push({
          connector: 'epic',
          operation: 'submit_attachment',
          ok: false,
          skipped: true,
          reason: !req.url ? 'Missing EPIC_FHIR_BASE_URL or endpoint path.' : 'Missing EPIC_ACCESS_TOKEN.',
        });
        continue;
      }

      const outcome = await executeRequest(req);
      const externalEmrId = clean(outcome.summary?.id, 120);
      executed.push({
        connector: 'epic',
        operation: 'submit_attachment',
        ok: outcome.ok,
        status: outcome.status,
        response: outcome.summary,
        externalEmrId,
      });
      if (outcome.ok && externalEmrId) {
        externalIds.push({
          connector: 'epic',
          operation: 'submit_attachment',
          externalEmrId,
          externalIdLabel: 'epic_attachment_id',
        });
      }
      continue;
    }

    if (preview.connector === 'athena') {
      const uploadReq = preview.requestChain[0];
      const taskReq = preview.requestChain[1];

      if (!uploadReq.url || !uploadReq.hasAuthorization) {
        executed.push({
          connector: 'athena',
          operation: 'upload_clinical_document',
          ok: false,
          skipped: true,
          reason: !uploadReq.url
            ? 'Missing ATHENA practiceId/patientId or ATHENAHEALTH_BASE_URL.'
            : 'Missing ATHENAHEALTH_ACCESS_TOKEN.',
        });
        continue;
      }

      const uploadOutcome = await executeRequest(uploadReq);
      const documentId = extractAthenaClinicalDocumentId(uploadOutcome.body);

      executed.push({
        connector: 'athena',
        operation: 'upload_clinical_document',
        ok: uploadOutcome.ok,
        status: uploadOutcome.status,
        response: uploadOutcome.summary,
        externalEmrId: clean(documentId, 120),
      });
      if (uploadOutcome.ok && documentId) {
        externalIds.push({
          connector: 'athena',
          operation: 'upload_clinical_document',
          externalEmrId: clean(documentId, 120),
          externalIdLabel: 'athena_clinical_document_id',
        });
      }

      if (!uploadOutcome.ok || !taskReq.url) {
        executed.push({
          connector: 'athena',
          operation: 'create_task',
          ok: false,
          skipped: true,
          reason: !uploadOutcome.ok
            ? 'Task skipped because clinical document upload failed.'
            : 'Missing ATHENA task URL.',
        });
        continue;
      }

      const taskRequest = {
        ...taskReq,
        body: buildAthenaTaskBody(packet, athena, documentId || '{{clinicalDocumentId}}'),
      };
      const taskOutcome = await executeRequest(taskRequest);
      const taskId = clean(taskOutcome.summary?.id, 120);
      executed.push({
        connector: 'athena',
        operation: 'create_task',
        ok: taskOutcome.ok,
        status: taskOutcome.status,
        response: taskOutcome.summary,
        externalEmrId: taskId,
      });
      if (taskOutcome.ok && taskId) {
        externalIds.push({
          connector: 'athena',
          operation: 'create_task',
          externalEmrId: taskId,
          externalIdLabel: 'athena_task_id',
        });
      }
    }
  }

  const primaryExternalId =
    externalIds.find((item) => item.connector === 'athena' && item.operation === 'create_task') ||
    externalIds.find((item) => item.connector === 'epic' && item.operation === 'submit_attachment') ||
    externalIds[0] ||
    null;

  return {
    dryRun: false,
    connector: connectorMode,
    packetId: clean(packetId || packet.case_id || packet.caseId, 120),
    operatorId: clean(operatorId, 120),
    previews,
    executed,
    externalIds,
    primaryExternalId,
  };
}

export function validateConnectorPrototypePayload(payload = {}) {
  const body = asObject(payload);
  if (!Object.keys(body).length) {
    return { ok: false, message: 'Request body must be a JSON object.' };
  }

  const connector = normalizeConnector(body.connector || 'both');
  const mode = normalizeMode(body.mode);
  const packet = asObject(body.packet);
  if (!Object.keys(packet).length) {
    return { ok: false, message: 'packet is required and must be an object.' };
  }

  const dryRun = mode ? mode !== 'live' : body.dryRun !== false;
  const packetId = clean(body.packetId || packet.case_id || packet.caseId || '', 120);
  const operatorId = clean(body.operatorId || body.userId || '', 120);
  const attachment = asObject(body.attachment);
  const epic = asObject(body.epic);
  const athena = asObject(body.athena);

  if (connector === 'athena' || connector === 'both') {
    const practiceId = clean(athena.practiceId || packet.practice_id || process.env.ATHENAHEALTH_PRACTICE_ID, 80);
    const patientId = clean(athena.patientId || packet.patient_id, 80);
    if (!practiceId || !patientId) {
      return {
        ok: false,
        message: 'athena.practiceId and athena.patientId (or packet.practice_id and packet.patient_id) are required.',
      };
    }
  }

  return {
    ok: true,
    data: {
      connector,
      packet,
      dryRun,
      mode: dryRun ? 'dry-run' : 'live',
      packetId,
      operatorId,
      attachment,
      epic,
      athena,
    },
  };
}
